let currentLang = 'en';

function init() {
    const navbar = document.querySelector('.navbar');
    const reveals = document.querySelectorAll('.reveal');

    window.addEventListener('scroll', () => {
        if (navbar) navbar.classList.toggle('scrolled', window.scrollY > 50);
        reveals.forEach(el => {
            if (el.getBoundingClientRect().top < window.innerHeight - 100) el.classList.add('active');
        });
    });
    window.dispatchEvent(new Event('scroll'));

    setTimeout(() => {
        const intro = document.getElementById('intro-screen');
        if (intro) { intro.classList.add('hide'); setTimeout(() => intro.remove(), 1500); }
        document.body.classList.remove('loading');
    }, 3500);

    const langToggle = document.getElementById('lang-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', () => {
            currentLang = currentLang === 'en' ? 'ar' : 'en';
            document.documentElement.lang = currentLang;
            document.documentElement.dir = currentLang === 'ar' ? 'rtl' : 'ltr';
            document.querySelector('.en-active').style.display = currentLang === 'en' ? 'inline' : 'none';
            document.querySelector('.ar-active').style.display = currentLang === 'ar' ? 'inline' : 'none';
            updateContent();
        });
    }

    // Project cards (editorial layout)
    document.querySelectorAll('.project-editorial').forEach(card => {
        card.addEventListener('click', () => openModal(card.dataset.projectId));
    });

    // Modal close
    const modal = document.getElementById('project-modal');
    document.getElementById('close-modal').addEventListener('click', () => {
        modal.classList.remove('active');
        document.body.classList.remove('modal-open');
    });
    modal.addEventListener('click', (e) => {
        if (e.target === modal) { modal.classList.remove('active'); document.body.classList.remove('modal-open'); }
    });

    function openModal(id) {
        const data = projectsData[id];
        if (!data) return;
        const m = data.metadata[currentLang];
        let gallery = '';
        data.images.forEach((src, i) => {
            gallery += `<div class="modal-img-block">
                <img src="${src}" alt="${m.title} ${i+1}" class="modal-img">
                <p class="modal-img-caption">${data.captions[i][currentLang]}</p>
            </div>`;
        });
        document.getElementById('modal-content-area').innerHTML = `
            <div class="modal-hero-img"><img src="${data.coverImage}" alt="${m.title}"></div>
            <div class="modal-intro">
                <h1 class="modal-title">${m.title}</h1>
                <div class="modal-meta-bar">
                    <span><strong>Type:</strong> ${m.type}</span>
                    <span><strong>Style:</strong> ${m.style}</span>
                    <span><strong>Year:</strong> ${m.year}</span>
                    <span><strong>Location:</strong> ${m.location}</span>
                </div>
                <p class="modal-description">${m.description}</p>
            </div>
            <div class="modal-gallery">${gallery}</div>`;
        modal.classList.add('active');
        document.body.classList.add('modal-open');
        modal.scrollTo(0, 0);
    }

    function updateContent() {
        document.querySelectorAll('[data-en][data-ar]').forEach(el => el.innerHTML = el.getAttribute('data-' + currentLang));
        document.querySelectorAll('[data-placeholder-en]').forEach(el => el.setAttribute('placeholder', el.getAttribute('data-placeholder-' + currentLang)));
    }
}

if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', init);
else init();
